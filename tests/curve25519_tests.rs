use rusty_ecc::curve25519::Curve25519Num;

#[test]
fn test_basic_ops() {
	let a: Curve25519Num = "336ECB6230A7CFC58B98DEAD7FC57E305E3525F6F89B0F871F22AE30CE1CBFF9".into();
	let b: Curve25519Num = "265827C75DBB7FAD781474B7795A4CFBCC39262E3DFFC28AF5766E1D9423829".into();

	assert_eq!(a * b, "309a159650a4152996d9bd95ca9615dd21c7eae86684ca126fd9a035d54bd7e0".into());
}